package Modelo;

public class Arca {
	
	int cantidadAlmacenada;
	int cantidadMaxima;
	int idArca;
	int denominacion;
	
	public int getCantidadAlmacenada() {
		return cantidadAlmacenada;
	}
	public void setCantidadAlmacenada(int cantidadAlmacenada) {
		this.cantidadAlmacenada = cantidadAlmacenada;
	}
	public int getCantidadMaxima() {
		return cantidadMaxima;
	}
	public void setCantidadMaxima(int cantidadMaxima) {
		this.cantidadMaxima = cantidadMaxima;
	}
	public int getIdArca() {
		return idArca;
	}
	public void setIdArca(int idArca) {
		this.idArca = idArca;
	}
	public int getDenominacion() {
		return denominacion;
	}
	public void setDenominacion(int denominacion) {
		this.denominacion = denominacion;
	}
	

}
